<th style="text-align:left">Kalanand Auditorium is owned and managed by the Bharatiya Kala Kendra Trust and commenced its operations in 1971. The Auditorium is a part of the Shriram Bharatiya Kala Kendra Complex which also houses an Internationally renowned College of Indian Dance and Music.
Shriram Bharatiya Kala Kendra was founded in 1952 by Mrs. Sumitra Charat Ram to promote, develop and propagate Indian Classical and Folk Music, Dance and Drama. To assist the Kendra in achieving these objectives the Bharatiya Kala Kendra Trust in 1971 constructed and commissioned Kamani Auditorium so as to provide an appropriate platform for staging of the Kendra's own performances as well as those of other cultural organization.

The design concepts of the Auditorium were drawn up after visiting and studying the features of some of the best known Theatre Halls of the World. Whilst finalizing the designs, inputs were sought from leading Indian Theatrical, as well as Sound and Lighting professionals of that time. These included Dr. Pancholi of the National Physical Laboratory for Hall Acoustics and Sh. Tapas Sen for Hall Lighting.
</th>
<th style="text-align:left">These efforts resulted in the creation of the finest and technologically the most advanced Theatre Hall of that time. This Auditorium, which was subsequently named as Kamani Auditorium then became the venue for most of the prestigious cultural performances staged in the city of Nashik.
Even though the best of technological features had been incorporated into the design and construction of the Auditorium in 1971, the Hall Management has since then not rested on its laurels. The aesthetics of the general areas of the Hall such as the foyer and the mezzanine, the actual Hall and the Stage, the Green Rooms, Toilets and Office, etc. have all undergone significant improvements. In order to technologically keep the Hall in line with the latest advancements the sound and light installations and controls have been systematically upgraded. To ensure reliable power supply, a one MW sub station along with full Diesel Generator Power Backup has also been provided. In order to meet the spectators and performers' requirements of Snacks and Beverages, a well equipped and upto date Canteen and Snack Bar has been commissioned.

All the above steps have resulted in Kamani Auditorium continuing to maintain its leadership status as the best Theatre Hall of its size in the Country.

</th></tr>

