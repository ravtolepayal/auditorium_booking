<p>Kalanand Auditorium, a unit of the Bharatiya Kala Kendra Trust, is the most prestigious Theatre Hall of Nashik. The 632 seater Auditorium has been a host to some of the best National and International Theatrical, Dance and Musical Performances. The Auditorium building is an elegant structure with aesthetically designed Public Areas, Main Hall, Green Rooms, etc.<br>
		The Auditorium is strategically located in the Mandi House area of New Delhi at 1, Copernicus Marg. Mandi House is well known as the cultural hub of the Capital. The Auditorium is well connected by Bus services as well as the Delhi Metro which has its station just 5 minutes walking distance from the Hall.<br>
			The Auditorium has one of the finest stages in the City of Delhi with a width of 15 meters and a depth of 14 meters. The Auditorium stage is ideal for demanding theatrical performances. To enable quick change of scenes and sets as well as elaborate lighting design, the stage is equipped with 21 flying bars with an upper height of over 14 meters.<br>
			The Auditorium has excellent acoustics. It is furthermore equipped with a professional quality sound system using JBL Speakers, Crown Amplifiers and a 32 Channel Soundcraft Mixer. The Auditorium also has its own sophisticated stage lighting with a large variety of professional lights such as Pars, Profiles, Victories, etc., which are connected through a Computerized Soft Patching System to a 48 channel, zero 88, Bullfrog Control Panel.<br>
			The Auditorium has a direct inward dialing telephone system to handle customer queries as well as a mobile communication system to enable interconnection between the stage and the control room. In order to enhance security, the Auditorium has an elaborate CCTV System.<br>

The Auditorium is proud to be considered as one of the finest Halls in India for presentation of prestigious performances.
